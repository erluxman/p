/// Enumeration of available icon types for Todo entities.
///
/// Each icon type represents a visual category for the todo item,
/// helping users quickly identify different types of tasks.
enum TodoIconType {
  /// Writing task (e.g., pen icon)
  writing,

  /// Reading task (e.g., books icon)
  reading,

  /// Exercise task
  exercise,

  /// Work task
  work,

  /// Personal task
  personal,

  /// Shopping task
  shopping,

  /// Default/unspecified task
  defaultIcon,
}

